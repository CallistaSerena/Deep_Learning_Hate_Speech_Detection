class TFIDFGRUClassifier(nn.Module):
  def __init__(self, input_dim, hidden_dim, dropout=0.5, output_dim=1):
    super().__init__()
    self.input_dim = input_dim
    self.hidden_dim = hidden_dim

    # Project TF-IDF features to a dense embedding suitable for GRU
    self.embedding_layer = nn.Linear(input_dim, hidden_dim)

    # Bi-directional GRU layer
    self.gru = nn.GRU(
        hidden_dim
        , hidden_dim
        , batch_first = True
        , bidirectional = True
    )

    self.dropout = nn.Dropout(dropout)

    self.fc = nn.Linear(hidden_dim * 2, output_dim)

  def forward(self, x):
    embedded = self.embedding_layer(x)
    embedded = embedded.unsqueeze(1) # Shape: (batch_size, 1, hidden_dim)
    gru_out, _ = self.gru(embedded)
    final = gru_out[:, -1, :]
    x = self.dropout(final)
    x = self.fc(x)
    return torch.sigmoid(x)

class TFIDFIterableDataset(Dataset):
  def __init__(self, features, labels):
    self.features = features
    self.labels = labels

  def __len__(self):
    return self.features.shape[0]

  def __getitem__(self, idx):
    feature_tensor = torch.tensor(
        self.features[idx].toarray().flatten()
        , dtype=torch.float
    )
    return {
        "features": feature_tensor,
        "labels": torch.tensor(self.labels[idx], dtype=torch.float)
    }

