class indobert_bigru(nn.Module):
  def __init__(self
               , model_name
               , hidden_dim = 128
               , dropout = 0.3):
    super().__init__()
    self.bert = AutoModel.from_pretrained(
        model_name
    )
    bert_dim = self.bert.config.hidden_size

    self.bigru = nn.GRU(
        bert_dim
        , hidden_dim
        , batch_first = True
        , bidirectional = True
    )

    self.dropout = nn.Dropout(dropout)

    self.fc = nn.Linear(
        hidden_dim*2
        , 1
    )

  def forward(self, input_ids, attention_mask):
    output = self.bert(
        input_ids = input_ids
        , attention_mask = attention_mask
    )

    seq = output.last_hidden_state
    gru_out, _ = self.bigru(seq)
    final = gru_out[:, -1, :]
    x = self.dropout(final)
    x = self.fc(x)
    return torch.sigmoid(x)

